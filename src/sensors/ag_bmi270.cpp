bool AgBmi270::Init(uint32_t DevAddr, DeviceIntrf * const pIntrf, Timer * const pTimer)
{
	if (Valid())
		return true;

	if (pIntrf == NULL)
		return false;

	uint8_t regaddr;
	uint8_t d;

	Interface(pIntrf);

	if (pIntrf->Type() == DEVINTRF_TYPE_I2C)
	{
		switch (DevAddr)
		{
			case 1:
			case BMI270_I2C_7BITS_DEVADDR1:
				DeviceAddress(BMI270_I2C_7BITS_DEVADDR1);
				break;
			case 0:
			case BMI270_I2C_7BITS_DEVADDR0:
			default:
				DeviceAddress(BMI270_I2C_7BITS_DEVADDR0);
				break;
		}
	}
	else
	{
		DeviceAddress(DevAddr);
	}

	// Read chip id
	regaddr = BMI270_CHIP_ID_REG;
	d = Read8(&regaddr, 1);

	if (d != BMI270_CHIP_ID)
	{
		return false;
	}

	if (pTimer != NULL)
	{
		vpTimer = pTimer;
	}

	Reset();

	DeviceID(d);
	Valid(true);

	regaddr =
	regaddr = BMI270_PWR_CONF_REG;
	Write8(&regaddr, 1, 0);

	msDelay(1);

	if (LoadConfig(s_BMI270CfgData, s_BMI270CfgDataLen) == false)
	{
		return false;
	}

	msDelay(30);

	regaddr = BMI270_INTERNAL_STATUS_REG;
	d = Read8(&regaddr, 1);

	printf("status %x\r\n", d);

	if ((d & BMI270_INTERNAL_STATUS_MESSAGE_INIT_OK) == 0)
	{
		printf("Init failed\r\n");
		return false;
	}

	if (vpTimer == nullptr)
	{
		regaddr = BMI270_CMD_REG;
		Write8(&regaddr, 1, BMI270_CMD_FIFO_FLUSH);

		msDelay(10);

		regaddr = BMI270_FIFO_CONFIG0_REG;
		d = Read8(&regaddr, 1) | BMI270_FIFO_CONFIG0_FIFO_TIME_EN;

		Write8(&regaddr, 1, d);

		vFifoDataFlag = BMI270_FIFO_DATA_FLAG_TIME;
		vFifoFrameSize = 2;
	}

	return true;
}

